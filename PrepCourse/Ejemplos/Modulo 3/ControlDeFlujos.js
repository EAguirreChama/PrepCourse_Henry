function Viajar (Destino) {
    if (Destino === "Brasil") {
        console.log ("Gire a la Izquierda");
    } else if (Destino === "Argentina") {
        console.log ("Gire a la Derecha");
    } else {
        console.log ("Te Cagaste Perdiendo");
    }
}

